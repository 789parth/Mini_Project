package com.example.miniproject.TrendingProduct;

import java.io.Serializable;

public class ProductModel implements Serializable {

    private String category_id;
    private String category_name;
    private String product_description;
    private String product_id;
    private String product_image;
    private String product_name;
    private int product_price;
    private int product_quantity;
    private String product_unit;
    private boolean trending_item;

    public ProductModel() {
        // Required for Firebase
    }

    // Getters and Setters

    public String getCategory_id() {
        return category_id;
    }

    public void setCategory_id(String category_id) {
        this.category_id = category_id;
    }

    public String getCategory_name() {
        return category_name;
    }

    public void setCategory_name(String category_name) {
        this.category_name = category_name;
    }

    public String getProduct_description() {
        return product_description;
    }

    public void setProduct_description(String product_description) {
        this.product_description = product_description;
    }

    public String getProduct_id() {
        return product_id;
    }

    public void setProduct_id(String product_id) {
        this.product_id = product_id;
    }

    public String getProduct_image() {
        return product_image;
    }

    public void setProduct_image(String product_image) {
        this.product_image = product_image;
    }

    public String getProduct_name() {
        return product_name;
    }

    public void setProduct_name(String product_name) {
        this.product_name = product_name;
    }

    public int getProduct_price() {
        return product_price;
    }

    public void setProduct_price(int product_price) {
        this.product_price = product_price;
    }

    public int getProduct_quantity() {
        return product_quantity;
    }

    public void setProduct_quantity(int product_quantity) {
        this.product_quantity = product_quantity;
    }

    public String getProduct_unit() {
        return product_unit;
    }

    public void setProduct_unit(String product_unit) {
        this.product_unit = product_unit;
    }

    public boolean isTrending_item() {
        return trending_item;
    }

    public void setTrending_item(boolean trending_item) {
        this.trending_item = trending_item;
    }
}